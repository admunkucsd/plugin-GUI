#include "gtest/gtest.h"

#include <DataBuffer.h>

TEST(DataBuffer, AddToBufferSanityTest) {
    DataBuffer db(32, 4*32);
    float data;
    int64 sampleNumbers;
    double timestamps;
    uint64 eventCodes;
    db.addToBuffer(&data, &sampleNumbers, &timestamps, &eventCodes, 1);
    ASSERT_EQ(db.getNumSamples(), 1);
}
